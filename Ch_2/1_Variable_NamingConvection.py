#Variable Naming Conventions:
#1. A variable name must start with a letter (a-z, A-Z) or an underscore (_).
#2. A variable name cannot start with a number (0-9).
#3. A variable name can only contain letters, numbers, and underscores.
#4. Variable names are case-sensitive (e.g., myVar and myvar are different variables).
#5. Variable names should not be a reserved keyword in Python (e.g., if, else, while, for, etc.).
#6. Variable names should be descriptive and meaningful to enhance code readability.
#7. Use lowercase letters and separate words with underscores for better readability (e.g., my_variable_name).
#8. Avoid using single-character variable names except for counters or iterators in loops.
#9. Avoid using special characters or spaces in variable names.
#10. Keep variable names concise but informative.